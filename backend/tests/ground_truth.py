def build_fixture(con: duckdb.DuckDBPyConnection) -> dict[str, Any]:
    """Compute every answer and assemble the fixture dict."""
    computers = {
        1: q1, 2: q2, 3: q3, 4: q4, 5: q5, 6: q6,
        7: q7, 8: q8, 9: q9, 10: q10, 11: q11,
    }
    questions: list[dict[str, Any]] = []
    for q_id in range(1, 12):
        entry: dict[str, Any] = {
            "id": q_id,
            "tier": TIERS[q_id],
            "query": QUESTIONS[q_id],
            "answer": computers[q_id](con),
            "tolerance": TOLERANCES[q_id],
            "expected_phrases": EXPECTED_PHRASES[q_id],
            "expected_citations": EXPECTED_CITATIONS[q_id],
            "expected_tool_name": EXPECTED_TOOL_NAMES[q_id],
            "expected_tool_args_partial": EXPECTED_TOOL_ARGS[q_id],
        }
        if q_id == 9:
            entry["rubric"] = Q9_RUBRIC
        questions.append(entry)
    return {
        "version": "1.0.0",
        "generated_at": datetime.now(UTC).isoformat(),
        "dataset_sha256": sha256(CSV_PATH.read_bytes()).hexdigest(),
        "questions": questions,
    }


def main() -> None:
    """Bring up the data layer, compute the 11 answers, write the JSON."""
    con = duckdb.connect(":memory:")
    load_entries(con)
    create_views(con)
    validate_loaded_data(con)
    fixture = build_fixture(con)
    OUTPUT_PATH.write_text(
        json.dumps(fixture, indent=2, default=_decimal_default) + "\n"
    )
    print(
        f"Wrote {OUTPUT_PATH} ({len(fixture['questions'])} questions)",
        file=sys.stderr,
    )


if __name__ == "__main__":
    main()
