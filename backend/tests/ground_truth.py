def q1(con: duckdb.DuckDBPyConnection) -> dict[str, Any]:
    """Tier 1: PCA entries in Jan 2025."""
    row = con.execute(
        """
        SELECT COUNT(*) AS entry_count
        FROM entries_v
        WHERE customer_code = 'PCA'
          AND release_year_month = '2025-01'
          AND NOT is_shell
        """
    ).fetchone()
    assert row is not None
    return {"entry_count": row[0]}


