def q5(con: duckdb.DuckDBPyConnection) -> dict[str, Any]:
    """Tier 2: effective duty rate for MHF, CN origin, Q1 2025.

    Formula per KB ``customer_profiles_qbr_metrics.txt`` METRIC DEFINITIONS:
        (Total Duties + Fees) / Total Entered Value * 100
    """
    row = con.execute(
        """
        SELECT
            SUM(total_duty_taxes_fees)         AS total_duty,
            SUM(entered_value)                 AS total_entered_value,
            CASE WHEN SUM(entered_value) > 0
                 THEN CAST(SUM(total_duty_taxes_fees) AS DOUBLE)
                      / SUM(entered_value) * 100.0
                 ELSE 0
            END                                 AS effective_rate_pct,
            COUNT(*)                            AS line_count
        FROM entry_lines_v
        WHERE customer_code = 'MHF'
          AND country_of_origin_code = 'CN'
          AND release_year_quarter = '2025-Q1'
          AND NOT is_shell
        """
    ).fetchone()
    assert row is not None
    return {
        "rate_pct": round(float(row[2]), 4),
        "total_duty": row[0],
        "total_entered_value": row[1],
        "line_count": row[3],
    }


def q6(con: duckdb.DuckDBPyConnection) -> dict[str, Any]:
    """Tier 2: entries on hold + hold rate vs. industry benchmark (5%; >8% warrants)."""
    row = con.execute(
        """
        SELECT
            COUNT(*)                                                  AS total_entries,
            COUNT(*) FILTER (WHERE on_hold)                           AS entries_on_hold,
            100.0 * COUNT(*) FILTER (WHERE on_hold) / COUNT(*)        AS hold_rate_pct
        FROM entries_v
        WHERE NOT is_shell
        """
    ).fetchone()
    assert row is not None
    rate = float(row[2])
    status = (
        "warrants_investigation" if rate > 8.0
        else "above"             if rate > 5.0
        else "below"
    )
    return {
        "entries_total": row[0],
        "entries_on_hold": row[1],
        "hold_rate_pct": round(rate, 4),
        "benchmark_pct": 5.0,
        "status": status,
    }


def q7(con: duckdb.DuckDBPyConnection) -> dict[str, Any]:
    """Tier 3: IEEPA % of total duty per customer, Feb-Mar 2025; rank desc.

    Uses ``entries_v`` columns (which already encode the per-entry MPF cap)
    so the IEEPA percentage denominator is correctly capped at the entry
    grain - re-deriving from line grain would over-count MPF.
    """
    rows = con.execute(
        """
        WITH per_customer AS (
            SELECT
                customer_code,
                COALESCE(SUM(total_ieepa_duty), 0)        AS ieepa,
                (
                    SUM(total_primary_duty)
                    + COALESCE(SUM(total_section_301_duty), 0)
                    + COALESCE(SUM(total_ieepa_duty), 0)
                    + SUM(total_mpf_capped)
                    + SUM(total_hmf)
                )                                          AS total_duty
            FROM entries_v
            WHERE release_date BETWEEN DATE '2025-02-01' AND DATE '2025-03-31'
              AND NOT is_shell
            GROUP BY customer_code
        )
        SELECT
            customer_code,
            ieepa,
            total_duty,
            CASE WHEN total_duty > 0
                 THEN 100.0 * ieepa / total_duty
                 ELSE 0
            END AS ieepa_pct
        FROM per_customer
        ORDER BY ieepa_pct DESC
        """
    ).fetchall()
    ranked = [
        {
            "customer_code": r[0],
            "ieepa": r[1],
            "total_duty": r[2],
            "ieepa_pct": round(float(r[3]), 4),
            "rank": i + 1,
        }
        for i, r in enumerate(rows)
    ]
    return {
        "ranked": ranked,
        "highest_customer_code": ranked[0]["customer_code"] if ranked else None,
    }


def q8(con: duckdb.DuckDBPyConnection) -> dict[str, Any]:
    """Tier 3: top 5 HTS codes by total duty for PCA + CN origin."""
    rows = con.execute(
        """
        SELECT
            hts_code,
            ANY_VALUE(hts_description)                AS hts_description,
            SUM(primary_duty)
                + COALESCE(SUM(section_301_duty), 0)
                + COALESCE(SUM(ieepa_duty), 0)
                + SUM(mpf)
                + SUM(hmf)                            AS total_duty,
            SUM(primary_duty)                         AS primary_duty,
            COALESCE(SUM(section_301_duty), 0)        AS section_301,
            COALESCE(SUM(ieepa_duty), 0)              AS ieepa,
            COUNT(*)                                  AS line_count,
            SUM(entered_value)                        AS entered_value
        FROM entry_lines_v
        WHERE customer_code = 'PCA'
          AND country_of_origin_code = 'CN'
          AND NOT is_shell
        GROUP BY hts_code
        ORDER BY total_duty DESC
        LIMIT 5
        """
    ).fetchall()
    return {
        "top_5": [
            {
                "hts_code": r[0],
                "hts_description": r[1],
                "total_duty": r[2],
                "primary_duty": r[3],
                "section_301": r[4],
                "ieepa": r[5],
                "line_count": r[6],
                "entered_value": r[7],
            }
            for r in rows
        ]
    }


def q9(con: duckdb.DuckDBPyConnection) -> dict[str, Any]:
    """Tier 3: QBR summary for SAG Q1 2025.

    Composes four sub-views: entry volume by month, duty breakdown,
    top sourcing countries, and hold rate.
    """
    volume_rows = con.execute(
        """
        SELECT release_year_month, COUNT(*) AS entry_count
        FROM entries_v
        WHERE customer_code = 'SAG'
          AND release_year_quarter = '2025-Q1'
          AND NOT is_shell
        GROUP BY release_year_month
        ORDER BY release_year_month
        """
    ).fetchall()

    duty_row = con.execute(
        """
        SELECT
            SUM(total_primary_duty)                   AS primary_duty,
            COALESCE(SUM(total_section_301_duty), 0)  AS section_301,
            COALESCE(SUM(total_ieepa_duty), 0)        AS ieepa,
            SUM(total_mpf_capped)                     AS mpf_capped,
            SUM(total_hmf)                            AS hmf
        FROM entries_v
        WHERE customer_code = 'SAG'
          AND release_year_quarter = '2025-Q1'
          AND NOT is_shell
        """
    ).fetchone()
    assert duty_row is not None

    country_rows = con.execute(
        """
        SELECT country_of_origin_code, COUNT(*) AS line_count
        FROM entry_lines_v
        WHERE customer_code = 'SAG'
          AND release_year_quarter = '2025-Q1'
          AND NOT is_shell
        GROUP BY country_of_origin_code
        ORDER BY line_count DESC
        LIMIT 5
        """
    ).fetchall()

    hold_row = con.execute(
        """
        SELECT
            COUNT(*)                                                  AS total,
            COUNT(*) FILTER (WHERE on_hold)                           AS on_hold,
            100.0 * COUNT(*) FILTER (WHERE on_hold) / COUNT(*)        AS rate
        FROM entries_v
        WHERE customer_code = 'SAG'
          AND release_year_quarter = '2025-Q1'
          AND NOT is_shell
        """
    ).fetchone()
    assert hold_row is not None

    return {
        "entry_volume_by_month": [
            {"month": r[0], "count": r[1]} for r in volume_rows
        ],
        "duty_breakdown": {
            "primary": duty_row[0],
            "section_301": duty_row[1],
            "ieepa": duty_row[2],
            "mpf_capped": duty_row[3],
            "hmf": duty_row[4],
            "total": (
                duty_row[0] + duty_row[1] + duty_row[2] + duty_row[3] + duty_row[4]
            ),
        },
        "top_countries": [
            {"country_of_origin_code": r[0], "line_count": r[1]} for r in country_rows
        ],
        "hold_summary": {
            "entries_total": hold_row[0],
            "entries_on_hold": hold_row[1],
            "hold_rate_pct": round(float(hold_row[2]), 4),
        },
    }


