def _decimal_default(obj: Any) -> str:
    """Serialize ``decimal.Decimal`` as a string to preserve precision.

    Standard ``json.dumps`` rejects Decimal; converting to float would lose
    precision on the duty/value sums. The eval suite parses these back as
    ``Decimal(...)`` for tolerance comparisons.
    """
    if isinstance(obj, Decimal):
        return str(obj)
    raise TypeError(f"unencodable type: {type(obj).__name__}")


